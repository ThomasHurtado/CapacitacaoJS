console.log(4+3)

let soma = 10 + 5; // Resultado: 15
console.log('Soma:' + soma);

let subtracao = 10 - 5; // Resultado: 5
console.log('Subtração:', subtracao);

let multiplicacao = 10 * 5; // Resultado: 50
console.log('Multiplicação:', multiplicacao);

let divisao = 10 / 2; // Resultado: 5
console.log('Divisão:', divisao);

let modulo = 10 % 3 ; // Resultado: 1
console.log('Módulo:', modulo);

let exponenciacao = 2 ** 3; // Resultado: 8
console.log('Exponenciação:', exponenciacao);

// Incremento e Decremento
let x = 5;
x++; // Agora x é 6
console.log('Incremento:', x);

let y = 5;
y--; // Agora y é 4
console.log('Decremento:', y);

// Operadores de Atribuição
let a = 10;
a += 5; // Agora a é 15
console.log('Atribuição de soma:', a);

let b = 10;
b -= 5; // Agora b é 5
console.log('Atribuição de subtração:', b);

let c = 10;
c *= 2; // Agora c é 20
console.log('Atribuição de multiplicação:', c);

let d = 10;
d /= 2; // Agora d é 5
console.log('Atribuição de divisão:', d);

// Funções matemáticas com Math
let raizQuadrada = Math.sqrt(25); // Resultado: 5
console.log('Raiz Quadrada:', raizQuadrada);

let valorAbsoluto = Math.abs(-10); // Resultado: 10
console.log('Valor Absoluto:', valorAbsoluto);

let potencia = Math.pow(2, 3); // Resultado: 8
console.log('Potência:', potencia);

// Arredondamento
let arredondar = Math.round(4.7); // Resultado: 5
console.log('Arredondar:', arredondar);

let arredondarCima = Math.ceil(4.3); // Resultado: 5
console.log('Arredondar para cima:', arredondarCima);

let arredondarBaixo = Math.floor(4.7); // Resultado: 4
console.log('Arredondar para baixo:', arredondarBaixo);

// Mínimo e Máximo
let min = Math.min(1, 2, 3); // Resultado: 1
console.log('Mínimo:', min);

let max = Math.max(1, 2, 3); // Resultado: 3
console.log('Máximo:', max);

// Números aleatórios
let aleatorio = Math.random(); // Retorna um número aleatório entre 0 e 1
console.log('Aleatório:', aleatorio);


